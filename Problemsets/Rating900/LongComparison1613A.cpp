void solve() {
    ll x1, p1, x2, p2;
    std::cin >> x1 >> p1 >> x2 >> p2;
    int min = std::min(p1,p2);
    p1-=min;
    p2-=min;
    if (p1>=7) std::cout << ">" << '\n';
    else if (p2>=7) std::cout << "<" << '\n';
    else {
        for (int i = 0; i < p1; ++i) x1 *= 10;
        for (int i = 0; i < p2; ++i) x2 *= 10;
        if (x1 == x2) std::cout << "=" << '\n';
        else if (x1 > x2) std::cout << ">" << '\n';
        else std::cout << "<" << '\n';
    }
}

int main() {
    FAST_IO;

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
