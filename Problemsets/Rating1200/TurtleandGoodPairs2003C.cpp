void solve() {
    int n;
    cin >> n;
    string s;
    cin >> s;

    for (int i = 0; i < n/2; i++) {
        cout << s[i] << s[n-i-1];
    }

    if (n&1) cout << s[n/2];
    cout << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

