using namespace std;

void solve() {
    int n, k;
    cin >> n >> k;
    int res = 0;
    if (n%2==1) {
        n-=k;
        res = 1;
    }

    k-=1;
    res += (n+k-1)/k;
    cout << res << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

