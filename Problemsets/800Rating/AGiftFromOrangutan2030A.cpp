const int MAX_INT = 1e9;
const int MIN_INT = -1e9;

template<typename T>
void read_vector(std::vector<T> &vec) {
    for (T &val : vec) std::cin >> val;
}

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);

    int mx = MIN_INT;
    int min = MAX_INT;

    for (int i = 0; i < n; i++) {
        int x; cin >> x;
        mx = std::max(mx, x);
        min = std::min(min, x);
    }

    cout << (mx-min)*(n-1) << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

