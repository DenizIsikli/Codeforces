using namespace std;

const int N = 1e6+5;
int a[N];

void solve() {
    int n;
    cin >> n;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++) {
        int x;
        cin >> x;
        a[i] -= x;
    }

    sort(a, a+n);
    if (a[0]+a[1]<0) {
        cout << "NO" << '\n';
    } else {
        cout << "YES" << '\n';
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    std::cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}

